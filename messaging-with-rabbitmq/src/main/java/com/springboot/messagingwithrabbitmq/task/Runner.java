package com.springboot.messagingwithrabbitmq.task;

import com.springboot.messagingwithrabbitmq.config.MessageReceiverConfig;
import com.springboot.messagingwithrabbitmq.receiver.Receiver;
import org.springframework.amqp.rabbit.core.RabbitTemplate;
import org.springframework.boot.CommandLineRunner;
import org.springframework.stereotype.Component;

import java.util.concurrent.TimeUnit;

/**
 * <p> Title: </p>
 *
 * <p> Description: </p>
 *
 * @author: Guo.Weifeng
 * @version: 1.0
 * @create: 2019/6/26 10:28
 */
@Component
public class Runner implements CommandLineRunner {
	private final RabbitTemplate rabbitTemplate;
	private final Receiver receiver;

	public Runner(Receiver receiver, RabbitTemplate rabbitTemplate) {
		this.receiver = receiver;
		this.rabbitTemplate = rabbitTemplate;
	}

	@Override
	public void run(String... args) throws Exception {
		System.out.println("Sending message...");
		rabbitTemplate.convertAndSend(MessageReceiverConfig.topicExchangeName, "foo.bar.baz", "Hello from RabbitMQ!");
		receiver.getCountDownLatch().await(10000, TimeUnit.MILLISECONDS);
	}
}